     public ArrayList<SearchStudent> ListSearch(String ValToSearch)
    {
        ArrayList<SearchStudent> SearchList = new ArrayList<SearchStudent>();
        
        Statement st;
        ResultSet rs;
        
        try{
            Connection con = getConnection();
            st = con.createStatement();
            String searchQuery = "SELECT * FROM student WHERE CONCAT(`CodSt`, `nume`, `prenume`,`cetatenie`,`datan`) LIKE '%"+ValToSearch+"%'";
            rs = st.executeQuery(searchQuery);
            
            SearchStudent search;
            
            while(rs.next())
            {
                search = new SearchStudent(
                                 rs.getInt("CodSt"),
                                 rs.getString("Nume"),
                                 rs.getString("Prenume"),
                                 rs.getString("Cetatenie"),
                                 rs.getString("DataN")
                                );
                SearchList.add(search);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        return SearchList;
    }
     public void findSearch()
    {
        ArrayList<SearchStudent> search = ListSearch(SearchText.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"CodSt","Nume","Prenume","Cetatenie","DataN"});
        Object[] row = new Object[5];
        
        for(int i = 0; i < search.size(); i++)
        {
            row[0] = search.get(i).getCodSt();
            row[1] = search.get(i).getNume();
            row[2] = search.get(i).getPrenume();
            row[3] = search.get(i).getCetatenie();
            row[4] = search.get(i).getDataN();
            model.addRow(row);
        }
       STable.setModel(model);
       
    }
    

