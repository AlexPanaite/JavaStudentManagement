    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
       try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/database", "root", "");
            String sql = "Select * from user where username=? and password=?";
            PreparedStatement pst = con.prepareStatement(sql);
            PreparedStatement pst1 = con.prepareStatement(sql);
            pst.setString(1, UserField.getText());
            pst.setString(2, PassField.getText());
            ResultSet rs = pst.executeQuery();
            pst1.setString(1, UserField.getText());
            pst1.setString(2, PassField.getText());
            ResultSet rs1 = pst1.executeQuery();

            while(rs1.next()){
                name = rs1.getString("username");
                role = rs1.getString("role");

            }
            
            if(role.equals("Student")){
            StudentMenu.setVisible(false);
            MatricolMenu.setVisible(false);
            AdminMenu.setVisible(false);
            RaportMenu.setVisible(false);
            UserMenu.setVisible(false);
            AddButtonC.setVisible(false);
            DeleteButtonC.setVisible(false);
            ClearButtonC.setVisible(false);
            UpdateButtonC.setVisible(false);    
            }
            if(role.equals("Profesor")){ 
            MatricolMenu.setVisible(false);
            AdminMenu.setVisible(false);
            RaportMenu.setVisible(false);
            UserMenu.setVisible(false);
            AddButtonS.setVisible(false);
            DeleteButtonS.setVisible(false);
            ClearMButton1.setVisible(false);
            UpdateButtonS.setVisible(false);
            }
            if(role.equals("Secretar")){
                UserMenu.setVisible(false);
                AddButtonC.setVisible(false);
                DeleteButtonC.setVisible(false);
                ClearButtonC.setVisible(false);
                UpdateButtonC.setVisible(false);
                
                
            }
            if (rs.next()){
                JOptionPane.showMessageDialog(null, "Login successful");
                LoginButton.setVisible(false);
                ClearButton.setVisible(false);
                UserLabel.setVisible(false);
                PassLabel.setVisible(false);
                UserField.setVisible(false);
                PassField.setVisible(false);
                MenuBar.setVisible(true);
                WelcomeLabel.setVisible(true);
                RankLabel.setVisible(true);
                LogoutButton.setVisible(true);
                WelcomeLabel.setText("Welcome, "+name);
                RankLabel.setText("Drepturi utilizator: "+ role);
                UserField.setText("");
                PassField.setText("");

            }
            else{
                JOptionPane.showMessageDialog(null, "Login failed");
                UserField.setText("");
                PassField.setText("");
            }
            con.close();

        }

        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

